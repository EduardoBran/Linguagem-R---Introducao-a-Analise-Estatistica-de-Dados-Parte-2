# - Para compreender a Teoria da Probabilidade precisamos entender 3 importantes conceitos: experimento, espaço da amostraevento e evento.


## Experimento

# - É o processo de medir ou observar uma atividade com o propósito de coletar dados.
# - Exemplo: jogar um dado.


## Espaço de amostra

# - Todos os possíveis resultados de um experimento.
# - Exemplo: ao jogar um dado, todos os resultados possíveis.


## Evento

# - um ou mais resultados de um experimento.
# - Exemplo: ao jogar o dados teremos um evento daquele experimento.


# Conclusão: ao jogar um dado temos um evento daquele experimento que vai ter um então um espaço da amostra.


## Evento simples

# - Um evento com um único resultado na sua forma mais básica, que não pode ser simplificado.



